package com.sid.dashboard.dto;

public class UserDTO {
	
	private String userId;
	private String firstName;
	private String lastName;
	private String email;
	private String contactNo;
	private String gender;
	private String dob;
	private String activeStatus;
	private String optOutStatus;
	private String accountCreatedDate;
	private String totalLoginCount;
	private String zipcode;
	private String city;
	private String state;
	private String country;
	
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getContactNo() {
		return contactNo;
	}
	public void setContactNo(String contactNo) {
		this.contactNo = contactNo;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getDob() {
		return dob;
	}
	public void setDob(String dob) {
		this.dob = dob;
	}
	public String getActiveStatus() {
		return activeStatus;
	}
	public void setActiveStatus(String activeStatus) {
		this.activeStatus = activeStatus;
	}
	public String getOptOutStatus() {
		return optOutStatus;
	}
	public void setOptOutStatus(String optOutStatus) {
		this.optOutStatus = optOutStatus;
	}
	public String getAccountCreatedDate() {
		return accountCreatedDate;
	}
	public void setAccountCreatedDate(String accountCreatedDate) {
		this.accountCreatedDate = accountCreatedDate;
	}
	public String getTotalLoginCount() {
		return totalLoginCount;
	}
	public void setTotalLoginCount(String totalLoginCount) {
		this.totalLoginCount = totalLoginCount;
	}
	public String getZipcode() {
		return zipcode;
	}
	public void setZipcode(String zipcode) {
		this.zipcode = zipcode;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
}
